package datatypes;

public class Float_Demo_Program {

	public static void main(String[] args) {
		//adding two float numbers
		float num=544;
		float num1=558;
		float a;
		a=num+num1;
		System.out.println("adding two float numbers :"  + a);
		
		
		//subtractions of two numbers
		float num2=456.555f;
		float num3=4678.5546f;
		float b;
		b=num2-num3;
		System.out.println("subtractions of two numbers :" + b);
		
		
		//multiplications of two numbers
		float num4=4445.4655555555f;
	    float num5=47788.577f;
	    float c;
	    c=num4*num5;
	    System.out.println("multiplication  of two numbers :" + c);
	    
	    
	    //division of two numbers
	    float num6=4484.890f;
	    float num7=45658458;
	    float d;
	    d=num6%num7;
	    System.out.println("division of two numbers :" + d);
	    
	}

}
