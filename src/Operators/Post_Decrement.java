package Operators;

public class Post_Decrement {

	public static void main(String[] args) {
		// post decrements
		int x = 200;
		int y = 500;
		// decrement the y;
		int sum = y--;
		System.out.println(" sum of the value is:" + sum);
		System.out.println("decrement the 'y' values is:" + y);
		// decrement the x;
		sum = x--;
		System.out.println("sum of the value is: " + sum);
		System.out.println("decrement the 'x' value is :" + x);

	}

}
