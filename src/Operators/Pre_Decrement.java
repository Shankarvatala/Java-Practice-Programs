package Operators;

public class Pre_Decrement {

	public static void main(String[] args) {
		//pre-decrements
		int a=500;
		int b=800;
		//decrement the a value
		int result=--a;
		System.out.println("decrement the a value is: "+a);
		System.out.println("the result of :"+result);
		//decrement the b value
		result=--b;
		System.out.println("decrement the b value is: "+b);
		System.out.println("the result of :"+result);

	}

}
