package Operators;

public class Relatonal_Operators {

	public static void main(String[] args) {
		int num = 456;
		int tem = 455;
		System.out.println(num == tem);
		System.out.println(num >= tem);
		System.out.println(num <= tem);
		System.out.println(num > tem);
		System.out.println(num < tem);
		System.out.println(num & tem);
		System.out.println(num | tem);
		
	}

}
