package Operators;

public class Unary_Operator_DemoPrograms1 {

	public static void main(String[] args) {
		int i=46547;
		int j=1445;
		int z=i+j;
		System.out.println("sum is:"+z);
		i=i+8;
		System.out.println("increment the i value:"+i);
		j=j+5;
		System.out.println("increment the j value:"+j);
		z=i+j;
		System.out.println("increment the i and j values and final sum is:"+z);
				
	}

}
