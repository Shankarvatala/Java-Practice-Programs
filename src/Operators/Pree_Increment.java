package Operators;

public class Pree_Increment {

	public static void main(String[] args) {
	   float x=100;
	   float y=1000;
	   float z=x+y;
	   System.out.println("addition sum of the value is:"+z);
	   ++x;
	   ++y;
	   z=x+y;
	   System.out.println("pree increment of the 'x' and 'y' value is:"+z);

	}

}
