package Operators;

public class Airthmetic_Operator {

	public static void main(String[] args) {
		// type casting 
		float a=45.999f;
		int b=(int)a;
		System.out.println(b);
		
		
	// addition of type casting 
		double c=55.9;
		double  m=9.9;
		int n;
		n=(int)(c+m);
		System.out.println("addition of type casting:"+n);
		
		
    // subtraction  of type casting 
		 int num=45844;
		 int num3=455754;
		 double num2;
		 num2=num-num3;
		 System.out.println("subtraction of type casting:"+num2);
		 
	// multiplication of type casting 
		 byte tem=74;
		 byte tem2=6;
		 int tem3; 
		 tem3=tem*tem2;
		 System.out.println("multiplication of type casting :"+ tem3);
		 
	// division of type casting
		 byte cas=45;
		 long hyd=677;
		 int cmd;
		 cmd=(int)(cas/hyd);
		 System.out.println("division of type casting:"+cmd);
		 
	//modulus of type casting
		 byte number=44;
		 byte number1=3;
		 int number3;
		 number3=(int)(number%number1);
		 System.out.println("modulus of type casting:"+number3);
		 
		 
		 
	}

}
