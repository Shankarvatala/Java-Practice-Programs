package strings;

public class Equal_And_EqualIgnoreCase {
	
	    public static void main(String[] args) {
	        String str1 = "JAVA";
	        String str2 = "java";
	        System.out.println("Equals: " + str1.equals(str2));
	        System.out.println("Equals Ignore Case: " + str1.equalsIgnoreCase(str2));
	    }
	}



